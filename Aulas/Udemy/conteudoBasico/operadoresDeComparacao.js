/* 

Operadores de comparação:

> maior que
>= maior que ou igual
< menor que 
<= menor que ou igual 
== igualdade (valor)
=== igualdade estrita (valor e tipo
!= diferente (valor)
!== diferente eestrita (valor e tipo)

*/

// Operador 'maior que'
console.log(5 > 3); // true

// Operador 'maior que ou igual'
console.log(5 >= 5); // true

// Operador 'menor que'
console.log(3 < 5); // true

// Operador 'menor que ou igual'
console.log(3 <= 3); // true

// Operador 'igualdade' (valor)
console.log('5' == 5); // true

// Operador 'igualdade estrita' (valor e tipo)
console.log('5' === 5); // false

// Operador 'diferente' (valor)
console.log('5' != 5); // false

// Operador 'diferente estrita' (valor e tipo)
console.log('5' !== 5); // true
