/* 

Operadores lógicos:
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NÃO

*/


// Operador lógico 'E' (&&)
console.log(true && false); // false

// Operador lógico 'OU' (||)
console.log(true || false); // true

// Operador lógico 'NÃO' (!)
console.log(!true); // false


/* 

O operador && retorna true se ambos os operandos forem verdadeiros.
O operador || retorna true se qualquer um dos operandos for verdadeiro.
O operador ! inverte o valor booleano do operando.

*/